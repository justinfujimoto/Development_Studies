/*

	Calculating Variables
	
	- Variables can caluclate expressions made up of nothing BUT variables
	
	var num1 = 1;
	var num2 = 2;
	var sum = num1 + num2;
	
	- A variable can be used in calculating its own new variable
	
	var num = 1;
	num = num + num;
	
	or 
	
	var num = 1;
	num = num + 2;
	
*/

